    /// <summary>
    /// Получить ответ по категории и теме
    /// </summary>
    public static string? GetAnswer(string category, string topic)
    {
        return category.ToLower() switch
        {
            "nutrition" => _nutritionAnswers.TryGetValue(topic, out var nutritionAnswer) ? nutritionAnswer : null,
            "workouts" => _workoutAnswers.TryGetValue(topic, out var workoutAnswer) ? workoutAnswer : null,
            "bot" => _botAnswers.TryGetValue(topic, out var botAnswer) ? botAnswer : null,
            _ => null
        };
    }

    /// <summary>
    /// Получить все доступные темы в категории
    /// </summary>
    public static List<string> GetAvailableTopics(string category)
    {
        return category.ToLower() switch
        {
            "nutrition" => _nutritionAnswers.Keys.ToList(),
            "workouts" => _workoutAnswers.Keys.ToList(),
            "bot" => _botAnswers.Keys.ToList(),
            _ => new List<string>()
        };
    }

